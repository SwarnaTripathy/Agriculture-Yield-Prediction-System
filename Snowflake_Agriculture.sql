--Advanced SQL 

--CTE
--#1 Average Yield Per Crop Per Year
SELECT 
    Crop_ID,
    Year,
    AVG(YIELD_VALUE) AS Avg_Yield
FROM AGRICULTURE_DB.WAREHOUSE.FACT_CROP_YIELD
GROUP BY Crop_ID, Year
ORDER BY Crop_ID, Year;

--#2 Year-over-Year Growth Using CTE
WITH YearlyYield AS (
    SELECT 
        Year,
        SUM(YIELD_VALUE) AS Total_Yield
    FROM AGRICULTURE_DB.WAREHOUSE.FACT_CROP_YIELD
    GROUP BY Year
),
YoY AS (
    SELECT 
        Year,
        Total_Yield,
        LAG(Total_Yield) OVER (ORDER BY Year) AS Previous_Year_Yield
    FROM YearlyYield
)
SELECT 
    Year,
    Total_Yield,
    Previous_Year_Yield,
    ROUND(
        ((Total_Yield - Previous_Year_Yield) 
        / Previous_Year_Yield) * 100, 2
    ) AS YoY_Growth_Percentage
FROM YoY
ORDER BY Year;

-- WINDOW FUNCTIONS
--#1 Running Average Yield Per Crop
SELECT 
    Crop_ID,
    Year,
    YIELD_VALUE,
    AVG(YIELD_VALUE) OVER (
        PARTITION BY Crop_ID
        ORDER BY Year
        ROWS BETWEEN UNBOUNDED PRECEDING AND CURRENT ROW
    ) AS Running_Avg_Yield
FROM AGRICULTURE_DB.WAREHOUSE.FACT_CROP_YIELD
ORDER BY Crop_ID, Year;


--#2 Year-over-Year Difference(LAG)
SELECT 
    Crop_ID,
    Year,
    YIELD_VALUE,
    LAG(YIELD_VALUE) OVER (
        PARTITION BY Crop_ID
        ORDER BY Year
    ) AS Previous_Year_Yield,
    
    YIELD_VALUE 
    - LAG(YIELD_VALUE) OVER (
        PARTITION BY Crop_ID
        ORDER BY Year
    ) AS Yield_Difference
FROM AGRICULTURE_DB.WAREHOUSE.FACT_CROP_YIELD
ORDER BY Crop_ID, Year;

